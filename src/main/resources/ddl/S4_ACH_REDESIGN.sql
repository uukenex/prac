-- 6. 찌 등급도 보정 (업적 달성자인데 장비가 낮으면)
UPDATE TBOT_S4_USER_EQUIP e
   SET e.BOBBER_GRADE = (
       SELECT MAX(a.REWARD_GRADE)
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'BOBBER_UNLOCK'
   )
 WHERE EXISTS (
       SELECT 1
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'BOBBER_UNLOCK'
   )
   AND e.BOBBER_GRADE < (
       SELECT MAX(a.REWARD_GRADE)
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'BOBBER_UNLOCK'
   );

