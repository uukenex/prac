-- 4. 잘못 적재된 장비 보정
--    도감완성 업적(ACH_ID 2,3,4) 달성자 → 낚시대 등급 자동 보정
--    (ROD_GRADE가 ACH의 REWARD_GRADE보다 낮으면 올려줌)
UPDATE TBOT_S4_USER_EQUIP e
   SET e.ROD_GRADE = (
       SELECT MAX(a.REWARD_GRADE)
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'ROD_UNLOCK'
   )
 WHERE EXISTS (
       SELECT 1
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'ROD_UNLOCK'
   )
   AND e.ROD_GRADE < (
       SELECT MAX(a.REWARD_GRADE)
         FROM TBOT_S4_USER_ACH ua
         JOIN TBOT_S4_ACHIEVEMENT a ON ua.ACH_ID = a.ACH_ID
        WHERE ua.USER_NAME = e.USER_NAME
          AND a.REWARD_TYPE = 'ROD_UNLOCK'
   );

-- 5. 누적 낚시 횟수 기준으로 신규 업적(ACH_ID 8,9,10) 소급 부여
--    10회 달성자: 10번째 낚시 날짜로 CLEAR_DATE 소급
INSERT INTO TBOT_S4_USER_ACH (USER_NAME, ACH_ID, CLEAR_DATE)
SELECT USER_NAME, 8, FISHING_DATE
  FROM (
       SELECT USER_NAME, FISHING_DATE,
              ROW_NUMBER() OVER (PARTITION BY USER_NAME ORDER BY FISHING_DATE) AS RN
         FROM TBOT_S4_FISHING_LOG
       )
 WHERE RN = 10
   AND USER_NAME NOT IN (SELECT USER_NAME FROM TBOT_S4_USER_ACH WHERE ACH_ID = 8);

--    30회 달성자
