-- 1. FIRST_CATCH_GRADE 업적: 보상 제거 (달성 표시만 유지)
UPDATE TBOT_S4_ACHIEVEMENT
   SET REWARD_TYPE = NULL, REWARD_GRADE = NULL
 WHERE ACH_TYPE = 'FIRST_CATCH_GRADE';

-- 2. 기존 도감완성 업적을 BOBBER → ROD_UNLOCK으로 변경
--    ACH_ID 2: ★1 도감완성 → 고급낚시대
UPDATE TBOT_S4_ACHIEVEMENT
   SET ACH_NAME = '★1 도감 완성!', REWARD_TYPE = 'ROD_UNLOCK', REWARD_GRADE = 2
 WHERE ACH_ID = 2;

--    ACH_ID 3: ★2 도감완성 → 희귀낚시대
UPDATE TBOT_S4_ACHIEVEMENT
   SET ACH_NAME = '★2 도감 완성!', REWARD_TYPE = 'ROD_UNLOCK', REWARD_GRADE = 3
 WHERE ACH_ID = 3;

--    ACH_ID 4: ★3 도감완성 → 영웅낚시대
UPDATE TBOT_S4_ACHIEVEMENT
   SET ACH_NAME = '★3 도감 완성!', REWARD_TYPE = 'ROD_UNLOCK', REWARD_GRADE = 4
 WHERE ACH_ID = 4;

-- 3. 신규 업적: 누적 낚시 횟수 → 찌 해금
--    (ACH_ID 8~10: 기존에 없으면 INSERT, 있으면 UPDATE)
MERGE INTO TBOT_S4_ACHIEVEMENT a
USING (SELECT 8 AS ACH_ID FROM DUAL) s ON (a.ACH_ID = s.ACH_ID)
WHEN MATCHED THEN
    UPDATE SET ACH_NAME = '낚시 10회 달성!', ACH_DESC = '낚시를 10번 완료했다!',
               ACH_TYPE = 'TOTAL_FISHING_COUNT', ACH_PARAM = '10',
               REWARD_TYPE = 'BOBBER_UNLOCK', REWARD_GRADE = 2
WHEN NOT MATCHED THEN
    INSERT (ACH_ID, ACH_NAME, ACH_DESC, ACH_TYPE, ACH_PARAM, REWARD_TYPE, REWARD_GRADE)
    VALUES (8, '낚시 10회 달성!', '낚시를 10번 완료했다!', 'TOTAL_FISHING_COUNT', '10', 'BOBBER_UNLOCK', 2);

